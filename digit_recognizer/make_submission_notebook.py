    "# Load Test Data\n",
    "test_df = pd.read_csv(\"test.csv\")\n",
    "test_dataset = DigitDataset(test_df.values, transform=val_transform)\n",
    "test_loader = DataLoader(test_dataset, batch_size=BATCH_SIZE, shuffle=False)\n",
    "\n",
    "aggregated_probs = torch.zeros(len(test_dataset), 10).to(DEVICE)\n",
    "\n",
