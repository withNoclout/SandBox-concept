    "    \n",
    "    probs = []\n",
    "    with torch.no_grad():\n",
    "        for images in test_loader:\n",
    "            images = images.to(DEVICE)\n",
    "            outputs = model(images)\n",
    "            batch_probs = torch.softmax(outputs, dim=1)\n",
    "            probs.append(batch_probs)\n",
    "            \n",
    "    full_probs = torch.cat(probs, dim=0)\n",
    "    aggregated_probs += full_probs\n",
    "\n",
    "# Final Prediction\n",
    "_, predictions = torch.max(aggregated_probs, 1)\n",
